/**@brief send function.
 */
void B_L_E_send(char info[], uint16_t data)
{
    uint16_t str_size_to_phone;
    char send_data[50] = {0};

    if(info == NULL)
        sprintf(send_data, "%i", data);
    else if (data == NULL)
        sprintf(send_data, "%s", info);
    else
        sprintf(send_data, "%s%i", info, data);

    uint8_t ble_to_phone[sizeof(send_data)];
    for(int i = 0; i < sizeof(send_data); i++)
    {
        if((uint8_t)send_data[i] == NULL)
        {
            str_size_to_phone = i;
            break;
        }
        else
            ble_to_phone[i] = (uint8_t)send_data[i];
    }

    ble_nus_data_send(&m_nus, ble_to_phone, &str_size_to_phone, m_conn_handle);
}